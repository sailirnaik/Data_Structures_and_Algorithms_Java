/*All divisors of a Number
Given an integer n, print all the divisors of N in the ascending order.
 
Examples:

Input : n = 20
Output: 1 2 4 5 10 20
Explanation: 20 is completely divisible by 1, 2, 4, 5, 10 and 20.
Input: n = 21191
Output: 1 21191
Explanation: As 21191 is a prime number, it has only 2 factors(1 and the number itself).
Constraints:
1 ≤ n ≤ 109

https://www.geeksforgeeks.org/problems/all-divisors-of-a-number/1
*/

class Solution {
    public static void print_divisors(int n) {

       int limit = (int)Math.sqrt(n);
    

        for (int i = 1; i <= limit; i++) {
            if (n % i == 0) {
                System.out.print(i + " ");
            }
        }
    
        for (int i = limit; i >= 1; i--) {
            if (n % i == 0) {
                if (limit!=n/i) {  
                    System.out.print(n/i + " ");
                }
            }
        }
    }

}